	PK_FactInternetSales varchar(50) primary key not null,
    SalesOrderNumber varchar(30) not null,
    OrderDateKey INT, 
	OrderDate date not null,
    DueDateKey INT,
    DueDate date not null,
    ShipDateKey INT,
    ShipDate date,
